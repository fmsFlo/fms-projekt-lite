const besoldungsgruppeSchema = z.object({
  bundesland: z.enum(BUNDESLAENDER, {
    message: 'Das ausgewählte Bundesland wird aktuell nicht unterstützt.',
  }),
  besoldungsgruppe: z.string().min(1, 'Besoldungsgruppe erforderlich.'),
  erfahrungsstufe: z
    .number()
    .int()
    .min(0, 'Erfahrungsstufe darf nicht negativ sein.')
    .max(20, 'Erfahrungsstufe ist zu hoch.'),
})

export function getGrundgehalt({
  bundesland,
  besoldungsgruppe,
  erfahrungsstufe,
}: {
  bundesland: Bundesland
  besoldungsgruppe: string
  erfahrungsstufe: number
}): number {
  const parsed = besoldungsgruppeSchema.parse({ bundesland, besoldungsgruppe, erfahrungsstufe })

  const landTabelle = BEAMTEN_BESOLDUNG[parsed.bundesland]
  if (!landTabelle) {
    throw new Error(`Keine Besoldungsdaten für Bundesland ${parsed.bundesland} verfügbar.`)
  }

  const gruppenTabelle = landTabelle[parsed.besoldungsgruppe]
  if (!gruppenTabelle) {
    throw new Error(`Besoldungsgruppe ${parsed.besoldungsgruppe} ist für ${parsed.bundesland} nicht hinterlegt.`)
  }

  const stufenKeys = Object.keys(gruppenTabelle)
  const verfuegbareStufen = stufenKeys.map((key) => Number.parseInt(key, 10)).sort((a, b) => a - b)
  if (!verfuegbareStufen.includes(parsed.erfahrungsstufe)) {
    throw new Error(
      `Erfahrungsstufe ${parsed.erfahrungsstufe} ist für ${parsed.besoldungsgruppe} nicht verfügbar. Zulässig sind: ${verfuegbareStufen.join(', ')}`,
    )
  }

  return gruppenTabelle[parsed.erfahrungsstufe]
}

export function getErtragsanteilForAge(age: number): number {
  if (!Number.isFinite(age)) return 27
  const roundedAge = Math.min(Math.max(Math.floor(age), 0), 100)
  if (roundedAge < 55) {
    return 27
  }
  // Ertragsanteil steigt linear von 27% bei 55 auf 100% bei 90
  if (roundedAge >= 55 && roundedAge <= 90) {
    const factor = (roundedAge - 55) / (90 - 55)
    return Math.round(27 + factor * (100 - 27))
  }
  return 100
}

export function getProvisionTaxableShare(type: ProvisionType, retirementAge?: number): number {
  if (type === 'Privatrente' && typeof retirementAge === 'number') {
    const ertragsanteil = getErtragsanteilForAge(retirementAge)
    return Math.max(0, Math.min(1, ertragsanteil / 100))
  }
  // Standard-Werte für steuerpflichtigen Anteil je nach Provisionstyp
  const defaultShares: Partial<Record<ProvisionType, number>> = {
    'Basis-Rente': 0.5,
    'Riester-Rente': 0.5,
    'Betriebliche Altersvorsorge': 0.5,
    'Immobilieneinkünfte': 1.0,
    'Sonstige Einkünfte': 1.0,
    'Depotwert nach Steuern': 0.0,
    'Sonstige wiederkehrende Bezüge': 1.0,
    'Sparen': 0.0
  }
  return defaultShares[type] ?? 0.5
}
